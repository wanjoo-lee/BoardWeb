package polymorphism;

public class LgTV {
	public void turnOn() {
		System.out.println("LgTV--전원켠다.");
	}
	public void turnOff() {
		System.out.println("LgTV--전원끈다.");
	}
	public void soundUp() {
		System.out.println("LgTV--소리올린다.");
	}
	public void soundDown() {
		System.out.println("LgTV--소리내린다.");
	}
}
